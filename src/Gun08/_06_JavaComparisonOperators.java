package Gun08;

public class _06_JavaComparisonOperators {
    public static void main(String[] args) {
        int a=50;
        int b=50;

        //atama isareti, == esit mi anlamindadir
        System.out.println("a b ye esit mi? = " +(a==b) );  //true
        System.out.println("a b den farkli mi? = " +(a!=b) );   //false

        System.out.println("a b ye buyuk mu? = " +(a>b) );  //false
        System.out.println("a b ye kucuk mu? = " +(a<b) );  //false

        System.out.println("a b den buyuk veya esitmi? = " +(a>=b) );   //true
        System.out.println("a b den kucuk veya esitmi? = " +(a<=b) );   //true



    }
}
